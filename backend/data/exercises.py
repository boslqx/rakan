def get_exercises_for_equipment(equipment_list: list[str]) -> list[dict]:
    """
    Returns exercises the user CAN do given their equipment.
    fullGym = return everything.
    Otherwise match ALL required equipment against user's list.
    """
    if "fullGym" in equipment_list:
        return EXERCISES

    available = []
    for ex in EXERCISES:
        can_do = all(eq in equipment_list for eq in ex["equipment"])
        if can_do:
            available.append(ex)
    return available


def filter_by_difficulty(exercises: list[dict], level: str) -> list[dict]:
    """
    Beginner    → beginner only
    Intermediate → beginner + intermediate
    Advanced    → all
    """
    if level == "beginner":
        allowed = {"beginner"}
    elif level == "intermediate":
        allowed = {"beginner", "intermediate"}
    else:
        allowed = {"beginner", "intermediate", "advanced"}

    return [ex for ex in exercises if ex["difficulty"] in allowed]